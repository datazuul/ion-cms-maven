# create tables

CREATE TABLE CATEGORIES(ID VARCHAR(250) PRIMARY KEY,METADATA LONGVARBINARY)
CREATE TABLE CATEGORIES_PUBLICATIONS(CATEGORY_ID VARCHAR(250),PUBLICATION_ID VARCHAR(250),CONSTRAINT SYS_PK_CATEGORIES_PUBLICATIONS PRIMARY KEY(CATEGORY_ID,PUBLICATION_ID))
CREATE TABLE GROUPS(ID VARCHAR(250) PRIMARY KEY,METADATA LONGVARBINARY)
CREATE TABLE PUBLICATIONS(ID VARCHAR(250) PRIMARY KEY,WORKFLOW_TYPE VARCHAR(250),TYPE_ID VARCHAR(250),DATEPUBLI VARCHAR(250),METADATA LONGVARBINARY)
CREATE TABLE SECTIONS(ID VARCHAR(250) PRIMARY KEY,PARENT_ID VARCHAR(250),NUMINDEX BIGINT,METADATA LONGVARBINARY)
CREATE TABLE SECTIONS_PUBLICATIONS(SECTION_ID VARCHAR(250),PUBLICATION_ID VARCHAR(250),CONSTRAINT SYS_PK_SECTIONS_PUBLICATIONS PRIMARY KEY(SECTION_ID,PUBLICATION_ID))
CREATE TABLE TYPE_PUBLICATIONS(ID VARCHAR(250) PRIMARY KEY,METADATA LONGVARBINARY)
CREATE TABLE USERS(LOGIN VARCHAR(250) PRIMARY KEY,PASSWORD VARCHAR(250),METADATA LONGVARBINARY)
CREATE TABLE USERS_GROUPS(LOGIN VARCHAR(250),GROUP_ID VARCHAR(250),CONSTRAINT SYS_PK_USERS_GROUPS PRIMARY KEY(LOGIN,GROUP_ID))
CREATE TABLE VERSIONS_PUBLICATIONS(ID VARCHAR(250) PRIMARY KEY,AUTHOR_ID VARCHAR(250),WORKFLOW_ID BIGINT,PUBLICATION_ID VARCHAR(250),VERSION INTEGER,DATA VARCHAR)

